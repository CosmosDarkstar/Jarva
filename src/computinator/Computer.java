package computinator;

public class Computer {
	@SuppressWarnings("unused")
	private double cpu, circuits;
	@SuppressWarnings("unused")
	private int ram, memory;

	public Computer(double cpu, double circuits, int ram, int memory) {
		this.circuits = circuits;
		this.cpu = cpu;
		this.ram = ram;
		this.memory = memory;

	}

}
